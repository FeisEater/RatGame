package rottapeli.resource;

/**
 * Collection of actions that are achieved via input.
 * @author Pavel
 */
public enum Input {
/** Value of an input that is not configured. */
    NOTMAPPED,
    
/** Player 1 moves right. */
    PLR1RIGHT,
/** Player 1 moves down. */
    PLR1DOWN,
/** Player 1 moves left. */
    PLR1LEFT,
/** Player 1 moves up. */
    PLR1UP,
    
/** Game is paused. */
    PAUSE
}
